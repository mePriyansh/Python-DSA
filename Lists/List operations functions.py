#List Functions

n_list=[10,20,30,40,50]

#1. len()
print("\nLength of List")
print(len(n_list))

#2. max()
print("\nMaximum value in List")
print(max(n_list))

#3. min()
print("\nMinimum value in List")
print(min(n_list))

#4. sum()
print("\nSum of List")
print(sum(n_list))

#5. average
print("\nAverage of List")
print(sum(n_list)/len(n_list))